-- ============================================================================
-- Table: Ticket
-- Schema: [Tickets]
-- Purpose: Stores the current state of ticket only (central transactional table)
-- Notes: One parent only. Root ticket reference is mandatory for tree-aware logic.
--          Requester can be resident OR internal user, not both at the same time.
--          IdaraID_FK is mandatory for filtering, reporting, dashboards.
-- ============================================================================

IF OBJECT_ID('[Tickets].[Ticket]', 'U') IS NOT NULL
    DROP TABLE [Tickets].[Ticket];
GO

CREATE TABLE [Tickets].[Ticket] (
    -- Primary Key
    [TicketID] BIGINT IDENTITY(1,1) NOT NULL,

    -- Unique Ticket Number (for external reference)
    [TicketNo] VARCHAR(20) NOT NULL,
    -- Format: TKT-YYYY-XXXXXX (generated by SP)

    -- IdaraID_FK is mandatory for filtering and reporting
    [IdaraID_FK] INT NOT NULL,

    -- Parent-Child Ticket Fields (for dependency tracking)
    [ParentTicketID_FK] BIGINT NULL, -- One parent only
    [RootTicketID_FK] BIGINT NULL, -- Required for tree reporting and cross-level aggregation

    -- Service Information
    [ServiceID_FK] BIGINT NULL, -- NULL for "Other" service type
    [IsOtherService] BIT NOT NULL CONSTRAINT DF_Ticket_IsOtherService DEFAULT 0,

    -- Ticket Classification
    [TicketClassID_FK] INT NOT NULL, -- Reference to [Tickets].[TicketClass]

    -- Requester Information
    [RequesterTypeID_FK] INT NOT NULL, -- Reference to [Tickets].[RequesterType]
    [RequesterUserID_FK] BIGINT NULL, -- Reference to existing User table (internal)
    [RequesterResidentID_FK] BIGINT NULL, -- Reference to resident table (external)
    [RequesterName] VARCHAR(200) NULL, -- Display name for external requesters
    [RequesterContact] VARCHAR(100) NULL, -- Contact info for external requesters

    -- Ticket Details
    [Title] VARCHAR(200) NOT NULL,
    [TitleAR] NVARCHAR(200) NULL, -- Arabic title for bilingual support
    [Description] VARCHAR(MAX) NULL,
    [DescriptionAR] NVARCHAR(MAX) NULL,
    [Location] VARCHAR(255) NULL,
    [LocationDetails] VARCHAR(500) NULL,

    -- Priority Management
    [SuggestedPriorityID_FK] INT NULL, -- Priority requested by user
    [EffectivePriorityID_FK] INT NOT NULL, -- Actual priority assigned (from service default or override)

    -- Status Management
    [TicketStatusID_FK] INT NOT NULL, -- Reference to [Tickets].[TicketStatus]
    [SubStatus] VARCHAR(50) NULL, -- Additional status context

    -- Routing Information (Queue routing is different from execution assignment)
    [CurrentDSDID_FK] BIGINT NOT NULL, -- Current organizational queue (reference to DSDID)
    [CurrentQueueDistributorID_FK] BIGINT NULL, -- Reference to existing Distributor table
    [AssignedUserID_FK] BIGINT NULL, -- Assigned to real user (execution assignment)
    [AssignedAt] DATETIME2 NULL,
    [AcceptedAt] DATETIME2 NULL,

    -- Work Execution
    [InProgressAt] DATETIME2 NULL,
    [OperationalResolvedAt] DATETIME2 NULL,
    [OperationalResolvedBy] BIGINT NULL,
    [FinallyClosedAt] DATETIME2 NULL,
    [FinallyClosedBy] BIGINT NULL,

    -- Quality Review Configuration
    [RequiresQualityReview] BIT NOT NULL CONSTRAINT DF_Ticket_RequiresQualityReview DEFAULT 1,
    [QualityReviewerID_FK] BIGINT NULL,

    -- Parent Blocking State
    [IsParentBlocked] BIT NOT NULL CONSTRAINT DF_Ticket_IsParentBlocked DEFAULT 0,
    [BlockedSince] DATETIME2 NULL,

    -- SLA Reference (computed/stored separately)
    [LastSLACalculatedAt] DATETIME2 NULL,

    -- Additional Metadata
    [Tags] VARCHAR(500) NULL,
    [CustomField1] VARCHAR(255) NULL,
    [CustomField2] VARCHAR(255) NULL,

    -- Audit Columns
    [CreatedAt] DATETIME2 NOT NULL CONSTRAINT DF_Ticket_CreatedAt DEFAULT SYSDATETIME(),
    [UpdatedAt] DATETIME2 NOT NULL CONSTRAINT DF_Ticket_UpdatedAt DEFAULT SYSDATETIME(),
    [CreatedBy] VARCHAR(100) NULL,
    [UpdatedBy] VARCHAR(100) NULL,

    -- Primary Key Constraint
    CONSTRAINT PK_Ticket PRIMARY KEY CLUSTERED ([TicketID])
);
GO

-- Foreign Key Constraints
