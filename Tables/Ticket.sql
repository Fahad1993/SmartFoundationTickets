ALTER TABLE [Tickets].[Ticket]
ADD CONSTRAINT FK_Ticket_Parent
    FOREIGN KEY ([ParentTicketID_FK]) REFERENCES [Tickets].[Ticket]([TicketID]);

ALTER TABLE [Tickets].[Ticket]
ADD CONSTRAINT FK_Ticket_Root
    FOREIGN KEY ([RootTicketID_FK]) REFERENCES [Tickets].[Ticket]([TicketID]);
