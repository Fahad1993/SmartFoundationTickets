    FOREIGN KEY ([SuggestedPriorityID_FK]) REFERENCES [Tickets].[Priority]([PriorityID]);

ALTER TABLE [Tickets].[Ticket]
ADD CONSTRAINT FK_Ticket_EffectivePriority
    FOREIGN KEY ([EffectivePriorityID_FK]) REFERENCES [Tickets].[Priority]([PriorityID]);

ALTER TABLE [Tickets].[Ticket]
ADD CONSTRAINT FK_Ticket_TicketStatus
    FOREIGN KEY ([TicketStatusID_FK]) REFERENCES [Tickets].[TicketStatus]([TicketStatusID]);

