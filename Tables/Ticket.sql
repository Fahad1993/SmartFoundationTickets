CREATE NONCLUSTERED INDEX IX_Ticket_ServiceID
ON [Tickets].[Ticket] ([ServiceID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_CurrentDSDID
ON [Tickets].[Ticket] ([CurrentDSDID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_AssignedUserID
ON [Tickets].[Ticket] ([AssignedUserID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_TicketStatusID
ON [Tickets].[Ticket] ([TicketStatusID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_ParentTicketID
ON [Tickets].[Ticket] ([ParentTicketID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_RootTicketID
ON [Tickets].[Ticket] ([RootTicketID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_RequesterUserID
ON [Tickets].[Ticket] ([RequesterUserID_FK]);

CREATE NONCLUSTERED INDEX IX_Ticket_CreatedAt
ON [Tickets].[Ticket] ([CreatedAt] DESC);

CREATE NONCLUSTERED INDEX IX_Ticket_EffectivePriorityID_CreatedAt
ON [Tickets].[Ticket] ([EffectivePriorityID_FK], [CreatedAt] DESC);

-- Composite index for inbox queries (by status + DSDID + created date)
CREATE NONCLUSTERED INDEX IX_Ticket_InboxQuery
ON [Tickets].[Ticket] ([TicketStatusID_FK], [CurrentDSDID_FK], [CreatedAt] DESC)
INCLUDE ([TicketNo], [Title], [EffectivePriorityID_FK], [AssignedUserID_FK]);
GO

-- Check Constraint: Requester cannot be both resident and internal user
ALTER TABLE [Tickets].[Ticket]
ADD CONSTRAINT CK_Ticket_RequesterType
    CHECK (
        (RequesterTypeID_FK IN (SELECT RequesterTypeID FROM [Tickets].[RequesterType] WHERE IsInternal = 1) AND RequesterUserID_FK IS NOT NULL AND RequesterResidentID_FK IS NULL) OR
        (RequesterTypeID_FK IN (SELECT RequesterTypeID FROM [Tickets].[RequesterType] WHERE IsInternal = 0) AND RequesterResidentID_FK IS NOT NULL AND RequesterUserID_FK IS NULL)
    );

-- Simplified Check: One of RequesterUserID_FK or RequesterResidentID_FK must be NULL
ALTER TABLE [Tickets].[Ticket]
ADD CONSTRAINT CK_Ticket_SingleRequester
    CHECK ((RequesterUserID_FK IS NULL) OR (RequesterResidentID_FK IS NULL));
GO

-- Comment: This is the central transactional table of the ticketing system.
-- CurrentDSDID_FK is the source of truth for routing.
-- Queue routing (via CurrentQueueDistributorID_FK) is separate from execution assignment (via AssignedUserID_FK).
-- RootTicketID_FK is required for all tree-aware queries and reporting.
-- Parent-child relationships enable dependency tracking between tickets.
-- IsParentBlocked flag indicates if this ticket is waiting for a child ticket to complete.
